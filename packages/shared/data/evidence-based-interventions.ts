// Helper: get interventions by app category
export function getInterventionsByCategory(category: TaskCategory): EvidenceBasedIntervention[] {
  return EVIDENCE_BASED_INTERVENTIONS.filter((i) => i.category === category);
}

// Helper: get intervention by ID
export function getInterventionById(id: string): EvidenceBasedIntervention | undefined {
  return EVIDENCE_BASED_INTERVENTIONS.find((i) => i.id === id);
}

// Helper: build condensed library summary for AI prompt (saves tokens)
export function buildLibrarySummaryForPrompt(): string {
  return EVIDENCE_BASED_INTERVENTIONS.map(
    (i) =>
      `${i.id}|${i.category}|${i.intervention}|${i.adaptations.join('; ')}|${i.timeOfDay}|${i.durationMinutes}min|${i.difficultyLevel}`
  ).join('\n');
}

// Helper: get all intervention IDs
export function getAllInterventionIds(): string[] {
  return EVIDENCE_BASED_INTERVENTIONS.map((i) => i.id);
}
